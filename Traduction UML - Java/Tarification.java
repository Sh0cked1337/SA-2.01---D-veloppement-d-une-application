public class Tarification {
    private double pleinTarif;

    public Tarification(double pleinTarif) {
        this.pleinTarif = pleinTarif;
    }

    public double getPleinTarif() {
        return pleinTarif;
    }

    public void setPleinTarif(double pleinTarif) {
        this.pleinTarif = pleinTarif;
    }
}